function robot = r_create(theta,d,a,alpha,ndof,types, dim)
% create a robot structure from denavit-hartenberg parameters



r = struct( 'theta', sym(theta), ...
    'd', sym(d), ...
    'a', sym(a), ...
    'alpha', sym(alpha), ...
    'type', char(types), ...
    'dim', dim, ...
    'ndof',ndof);

robot = r;
end